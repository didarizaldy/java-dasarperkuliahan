    private void BtnCari1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCari1ActionPerformed
        // Tombol cari
        try
        {
            Connection conn = Koneksi.getConnection();
            String search = txtCari1.getText();
            ResultSet dat = conn.createStatement().executeQuery("SELECT * FROM barang WHERE nama_barang like '%"+search+"%' OR kode_barang like '%"+search+"%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            dtm.setRowCount(0);
            String [] data = new String[5];
            int i = 1;

            while(dat.next())
            {
                data[0] = dat.getString("kode_barang");
                data[1] = dat.getString("nama_barang");
                data[2] = dat.getString("harga");
                data[3] = dat.getString("satuan");
                data[4] = dat.getString("stok");
                dtm.addRow(data);
                i++;
            }
            dat.close();
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_BtnCari1ActionPerformed

